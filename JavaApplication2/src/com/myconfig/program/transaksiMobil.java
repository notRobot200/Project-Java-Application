    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // TODO add your handling code here:
        conn = connect_db.configDB();
        try{
            if(this.jDateChooser1.getDate()==null || this.jDateChooser2.getDate()==null || this.field_lama.getText().isEmpty() || this.field_total.getText().isEmpty()){
                throw new Exception("Maaf, isi tanggal terlebih dahulu lalu hitung!!!");
            }
            String Sql = "UPDATE db_mobil SET status=? WHERE nopol=? AND status='READY'";
            String Sql2 = "INSERT INTO menyewa (tgl_mulai, tgl_akhir, lama, total, user_id, mobil_id) VALUES (?, ?, ?, ?, ?, ?)";
            String Sql3 = "SELECT id_mobil FROM db_mobil WHERE nopol='"+this.nopol+"'";
        
            SimpleDateFormat tgl_mulai = new SimpleDateFormat("yyyy-MM-dd");
            String strtgl_mulai = tgl_mulai.format(jDateChooser1.getDate() );
            SimpleDateFormat tgl_akhir = new SimpleDateFormat("yyyy-MM-dd");
            String strtgl_akhir = tgl_akhir.format(jDateChooser2.getDate() );
            pat = (PreparedStatement)conn.prepareStatement(Sql3);
            rs = pat.executeQuery();
            rs.next();
            System.out.println(rs.getString("id_mobil"));
            pat = (PreparedStatement)conn.prepareStatement(Sql);
            pat.setString(1, "NOT READY");
            pat.setString(2, nopol);
            int count = pat.executeUpdate();
            if(count == 0){
                throw new Exception("Maaf mobil tidak tersedia");
            }
            pat = (PreparedStatement)conn.prepareStatement(Sql2);
            pat.setString(1, strtgl_mulai);
            pat.setString(2, strtgl_akhir);
            pat.setString(3, field_lama.getText());
            pat.setString(4, field_total.getText());
            pat.setString(5, this.user_id);
            pat.setString(6, rs.getString("id_mobil"));
            pat.execute();
            JOptionPane.showMessageDialog(this, "Mobil telah berhasil disewa");
            this.showTableData();
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_submitActionPerformed

    private void btn_hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hitungActionPerformed
        // TODO add your handling code here:
        try {
            SimpleDateFormat df = new SimpleDateFormat("dd-MM-yyyy");
            String strDate1 = df.format(jDateChooser1.getDate());
            String strDate2 = df.format(jDateChooser2.getDate());
            java.util.Date Tanggal1 = df.parse(strDate1);
            java.util.Date Tanggal2 = df.parse(strDate2);
            long Hari1 = Tanggal1.getTime();
            long Hari2 = Tanggal2.getTime();
            long diff = Hari2 - Hari1;
            long Lama = diff / (24 * 60 * 60 * 1000);
            String Hasil = (Long.toString(Lama));
            this.field_lama.setText(Hasil);

            int harga_sewa = Integer.parseInt(this.field_harga.getText()); // lbl_harga di pars ke integer
            int lama_sewa = Integer.parseInt(this.field_lama.getText());   // txt_lama di pars ke integer
            int Total = harga_sewa * lama_sewa;                     // mencari total Harga
            String a = Integer.toString(Total);
            this.field_total.setText(a);



        } catch (NumberFormatException | ParseException a) {
            JOptionPane.showMessageDialog(this, "Masukan Tanggal Peminjaman dan Tanggal Pengembalian");
        }
    }//GEN-LAST:event_btn_hitungActionPerformed

