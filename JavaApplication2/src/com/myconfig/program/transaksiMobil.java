    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:
        this.nopol = this.field_nopol.getText();
        String sql = String.format("SELECT * FROM db_mobil WHERE nopol='%s' LIMIT 1", this.field_nopol.getText());
        try {
            pat = (PreparedStatement)conn.prepareStatement(sql);
            rs = pat.executeQuery();
            int i = 0;
            while (rs.next()) {
                this.field_brand.setText(rs.getString("brand"));
                this.field_model.setText(rs.getString("model"));
                this.field_status.setText(rs.getString("status"));
                this.field_tahun.setText(rs.getString("tahun"));
                this.field_harga.setText(rs.getString("harga"));
                this.field_warna.setText(rs.getString("warna"));
                i++;
            }
            if(i==0){
                throw new SQLException("Maaf nopol tidak sesuai");
            }
            
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_cariActionPerformed

